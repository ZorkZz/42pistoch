#include <unistd.h>

void	ft_is_negative(int n)
{
	int	l;

	l = 'P';
	if (n >= 0)
	{
		l = 'P';
	}
	else if (n < 0)
	{
		l = 'N';
	}
	write(1, &l, 1);
}
