void	swap(int *a, int *b)
{
	int	d;

	d = *a;
	*a = *b;
	*b = d;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	z;

	z = 0;
	while (z < size / 2)
	{
		swap(&tab[z], &tab[size - 1 - z]);
		z++;
	}
}
