int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 1;
	while (sqrt <= nb / sqrt)
	{
		if ((sqrt * sqrt) == nb)
			return (sqrt);
		sqrt ++;
	}
	return (0);
}
