#include <unistd.h>

void	print_table(int **tab_box)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while(i < 4)
	{
		while (j < 4)
		{
			write(1, &tab_box[i][j], 1);
			j++;
		}
		i++;
	}
}
